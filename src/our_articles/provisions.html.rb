<p><strong>Whereas we the
people are created equal, and</strong></p><p><strong>whereas
we the people are endowed with certain inalienable rights,
and</strong></p><p><strong>whereas we the people instituted a
government to secure these rights, and</strong></p><p><strong>whereas
we the people lay the foundation on such principles, and organize
its power in such form, as to us shall seem most likely to effect
the above objective, do require the following Bill of Rights for Voting
Equality.</strong></p><p><strong>&nbsp;</strong></p><p>&nbsp;</p><p>1.&nbsp;&nbsp;</p><p>Each
citizen of the United States at or exceeding the age of majority has the
right to vote in any public election in the jurisdiction where he or
she resides. That right shall not be denied or abridged by the United
States, any State, agreement, person, or entity. After incarceration
all rights shall resume.</p><p>&nbsp;</p><p>2.</p><p>a) All citizens
of the United States, residing in all states, shall have equal access,
(the same requirements), to creating a political party and achieving a
ballot line.</p><p>b) All candidates and parties shall have equal time
constraints to qualify for ballot access.&nbsp;</p><p>c) All proofs
will be received by a multi-partisan regulatory board, such as the
Board of Elections.</p><p>d) All citizens that desire to be candidates,
shall register at their local Board of Elections.</p><p>e) The Board
of Elections shall divide equally, the campaign tools for election
purposes. All tools must be properly labeled as having been provided by
the revenue stream and not a direct private donation.&nbsp;</p><p>f)
Elections shall be publicly funded. No private money may be used
for a public office, or seat in the government. The citizen must
have full confidence that no bribery or appearance of bribery is
taking place.</p><p>&nbsp;</p><p>&nbsp;</p><p>3.&nbsp;</p><p>The
District constituting the seat of Government of the United States
shall elect Senators and&nbsp; Representatives in the Congress
in such number and such manner as it&nbsp; would be entitled if
it were a State.</p><p>&nbsp;</p><p>4.</p><p>All citizens must
be able to verify that the vote has been counted accurately. All
ballots must be counted by hand. All counting must be supervised by
multi-partisan personnel and recorded.</p><p>&nbsp;</p><p>5.</p><p>a)
There shall be at least one Representative to each Thirty Thousand
citizens, per state.</p><p>b) Each state shall divide its population
by 30,000 to determine its number of representatives.</p><p>c) Each
Representative shall have the voting power equal to the number of
citizens that voted for them.</p><p>d) Congress shall be unicameral,
and the Senate shall be dissolved.</p><p>&nbsp;</p><p>6.</p><p>All
citizens shall have equal early voting hours in which to
cast their vote. sufficient voting places, materials, and
personnel shall be provided to reduce the voting time to within an
hour.</p><p>&nbsp;</p><p>7.</p><p>The Presidential/Vice-Presidential
election shall be counted by (score or approval)
counting.</p>
